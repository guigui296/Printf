#include "ft_printf.h"

int	ft_printu(int nb)
{
	long	n;
	int		l;

	n = nb;
	l = 0;
	if (n >= 10)
	{
		l += ft_printu (n / 10);
		l += ft_printu (n % 10);
	}
	if (n < 10)
		l += ft_printchar(n + '0');
	return (l);
}